CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  email VARCHAR(254) NOT NULL,
  name VARCHAR(250) NOT NULL,
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  name VARCHAR(50) NOT NULL,
  CONSTRAINT UQ_CATEGORY_NAME UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  category_id BIGINT NOT NULL REFERENCES categories(id) ON DELETE RESTRICT,
  annotation VARCHAR(2000) NOT NULL,
  created_on TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
  description VARCHAR(7000),
  event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  initiator BIGINT NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
  location_lat NUMERIC(9, 6) NOT NULL,
  location_lon NUMERIC(9, 6) NOT NULL,
  paid BOOLEAN NOT NULL DEFAULT FALSE,
  participant_limit INT NOT NULL DEFAULT 0,
  published_on TIMESTAMP WITHOUT TIME ZONE,
  request_moderation BOOLEAN NOT NULL DEFAULT TRUE,
  state VARCHAR(10) NOT NULL DEFAULT 'PENDING',
  title VARCHAR(120) NOT NULL,
  CONSTRAINT check_event_state CHECK (state IN ('PENDING', 'PUBLISHED', 'CANCELED'))
);

CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  event_id BIGINT NOT NULL REFERENCES events(id) ON DELETE RESTRICT,
  requester_id BIGINT NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
  status VARCHAR(10) DEFAULT 'PENDING',
  created TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT NOW(),
  CONSTRAINT check_request_status CHECK (status IN ('PENDING', 'CONFIRMED', 'REJECTED', 'CANCELED'))
);

CREATE TABLE IF NOT EXISTS compilations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  title VARCHAR(50) NOT NULL,
  pinned BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS compilation_event_link (
  compilation_id BIGINT NOT NULL REFERENCES compilations(id) ON DELETE RESTRICT,
  event_id BIGINT NOT NULL REFERENCES events(id) ON DELETE RESTRICT,
  PRIMARY KEY(compilation_id, event_id)
);